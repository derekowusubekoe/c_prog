#include <stdio.h>

int main()
{
    // arithmetic operations with (+) addition, (-) subtraction, (*) multiplication and (/) division
    int y = 2;
    int x = y + 1;
    x = x * 5;
    int u = x / 3;
    int m = u - 8;
    printf("y = %d\n", y);
    printf("x = %d\n", x);
    printf("u = %d\n", u);
    printf("m = %d\n", m);
    return 0;

}